    private void configuracionInicial() {
        
        int[] anchos = {20,350};

        for (int i = 0; i < this.tblUsuario.getColumnCount(); i++) {
            this.tblUsuario.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }
        
        this.controladorUsuarios = new ControladorUsuarios(this);
        this.btnNuevo.addActionListener(controladorUsuarios);
        this.btnModificar.addActionListener(controladorUsuarios);
        this.btnEliminar.addActionListener(controladorUsuarios);
        this.btnCerrar.addActionListener(controladorUsuarios);
        this.txtBuscar.addKeyListener(controladorUsuarios);
        this.addWindowListener(controladorUsuarios);
        
        this.setLocationRelativeTo(null);
        
    }

